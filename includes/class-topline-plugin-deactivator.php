/**
 * Fired during plugin deactivation.
 *
 * This class defines all code necessary to run during the plugin's deactivation.
 *
 * @since      1.0.0
 * @package    Topline_Plugin
 * @subpackage Topline_Plugin/includes
 * @author     Your Name <email@example.com>
 */
class Topline_Plugin_Deactivator {

	/**
	 * Short Description. (use period)
	 *
	 * Long Description.
	 *
	 * @since    1.0.0
	 */
	public static function deactivate() {

		$site 		=	get_bloginfo('name');
		$url 		=	get_bloginfo('wpurl'); 
		$email 		=	get_bloginfo('admin_email'); 
		$version 	=	get_bloginfo('version');
		
		$response = GuzzleHttp\post('http://topline.30lines.com/topline-uninstalled', [
			'body' => [
				'site' => $site,'wpurl' => $url,'email' => $email,'version' => $version
			]
		]);
	}

}
